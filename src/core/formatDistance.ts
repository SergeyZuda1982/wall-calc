/**
 * formatDistance.ts — форматирование расстояния для инструмента измерения
 * в 3D-виде (Scene3D.tsx, MeasureOverlay). Монтажнику удобнее видеть
 * миллиметры на небольших расстояниях (шаг сетки каркаса, зазоры) и метры
 * на расстояниях уровня комнаты — единая функция, чтобы поведение было
 * предсказуемым в одном месте.
 */

/**
 * Форматирует расстояние в метрах для показа человеку:
 * - меньше 1 м — в миллиметрах, целое число (мм точнее для мелких зазоров)
 * - 1 м и больше — в метрах, 2 знака после запятой
 */
export function formatDistanceM(distanceM: number): string {
  if (!Number.isFinite(distanceM) || distanceM < 0) return '—'
  if (distanceM < 1) {
    return `${Math.round(distanceM * 1000)} мм`
  }
  return `${distanceM.toFixed(2)} м`
}
