/**
 * calcSealingTape.ts — уплотнительная лента (герметик) по контуру примыкания
 * каркаса к строительным конструкциям.
 *
 * Правило (подтверждено пользователем, сессия 04.07.2026, разрез из
 * альбома Кнауф 1.031.9-2.07.2-6): лента клеится
 *   1. на ПН у потолка — по всей длине верхней направляющей;
 *   2. на ПН у пола — по всей длине нижней направляющей (за вычетом
 *      разрывов под дверными проёмами — там направляющей физически нет);
 *   3. на "пристенные" стойки (kind='wall') — те концевые стойки, что
 *      реально примыкают к капитальной стене/соседней конструкции —
 *      по всей их высоте.
 * Дверные/оконные торцевые стойки (kind='door'/'window') ленту НЕ получают —
 * они стоят внутри перегородки, ни к чему постороннему не примыкают.
 *
 * Универсально для любого типа перегородки (С111/С112 уже сейчас, С115-
 * серия — когда появится механика двойного каркаса: там просто вызывается
 * дважды, по разу на каждый независимый каркас).
 *
 * НЕ входит сюда (сознательно, по решению пользователя):
 * - разделительная лента и герметик у примыкания к соседним поверхностям —
 *   это отделочный этап (малярка), не материал каркаса;
 * - штучные самоклеящиеся отрезки L=200мм/шаг 600мм для С115.1/С115.3 —
 *   отдельная функция (см. calcDoubleFrameTapeStrips), не входит в общий
 *   метраж, т.к. считается штуками, а не погонными метрами, и появится
 *   вместе с механикой двойного каркаса.
 */

export interface SealingTapeResult {
  /** Погонные метры: ПН потолок + ПН пол (без дверных разрывов) + высота пристенных стоек */
  tapeLm: number
}

export function calcSealingTape(
  ceilTrackLenMm: number,
  floorTrackLenMm: number,
  studInfos: { kind: string; height: number }[],
): SealingTapeResult {
  const wallStudsHeightMm = studInfos
    .filter(si => si.kind === 'wall')
    .reduce((s, si) => s + si.height, 0)

  const tapeLm = (ceilTrackLenMm + floorTrackLenMm + wallStudsHeightMm) / 1000
  return { tapeLm }
}

/**
 * Штучные самоклеящиеся отрезки ленты (L=200мм) между стойками двух
 * независимых каркасов С115.1/С115.3 — там, где нет листа-разделителя
 * (в отличие от С115.2), значит нахлёст ставится в каждой паре стоек по
 * шагу. Пока не подключено ни к чему (нет механики двойного каркаса) —
 * функция готова заранее, чтобы не тащить эту деталь как долг.
 */
export function calcDoubleFrameTapeStrips(lengthMm: number, stepMm: number): number {
  if (lengthMm <= 0 || stepMm <= 0) return 0
  return Math.floor(lengthMm / stepMm) + 1
}
