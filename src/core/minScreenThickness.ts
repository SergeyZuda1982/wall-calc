/**
 * minScreenThickness.ts — расчёт коэффициента масштаба для тонких элементов
 * 3D-каркаса (профиль CD/ПП, подвесы), чтобы они не "терялись" визуально
 * при взгляде издалека (см. KONSPEKT-снапшот сессии 09.07.2026, п.1 списка
 * идей для 3D-вида).
 *
 * Идея из CAD-инструментов: тонкие элементы держат МИНИМАЛЬНЫЙ размер на
 * экране в пикселях независимо от расстояния до камеры — чем дальше объект,
 * тем больше его нужно визуально "раздуть" в поперечном сечении, чтобы он
 * не сжался до долей пикселя и не пропал из вида.
 *
 * ⚠️ 13.07.2026: по умолчанию (`maxScale=1`) этот приём фактически
 * ВЫКЛЮЧЕН — пользователь явно попросил честный масштаб без исключений:
 * профиль/подвес всегда рисуются в реальном физическом размере, тонкой
 * чертой издалека, корректно только при реальном приближении камеры.
 * Раздувание (было сначала 8x, потом 2x) убрано полностью. Код и параметр
 * `maxScale` оставлены — можно вернуть частичное раздувание, передав
 * явный `maxScale > 1` в конкретном вызове.
 *
 * Чистая математика, без three.js — легко тестируется без WebGL/канваса.
 * Использование (three.js-обвязка) — см. useMinThicknessScale в
 * components/CeilingGridMesh.tsx.
 */

export interface MinThicknessScaleParams {
  /** Расстояние от камеры до элемента, метры (мировые координаты). */
  distanceM: number
  /** Вертикальный угол обзора камеры (fov), радианы. */
  fovYRad: number
  /** Высота вьюпорта в пикселях. */
  viewportPxHeight: number
  /** Желаемый минимальный видимый размер элемента на экране, пиксели. */
  minPx: number
  /**
   * Реальный физический размер тонкого измерения элемента в МИРОВЫХ
   * координатах (метры) — то есть уже с учётом любых родительских
   * масштабов сцены (например, кнопок визуального масштаба 1x/5x/10x),
   * но БЕЗ учёта коэффициента, который эта функция как раз и считает.
   */
  actualWorldSizeM: number
  /**
   * Верхний предел коэффициента — чтобы элемент не "распухал" до абсурда
   * на очень большом удалении (когда объект виден целиком с большого
   * расстояния). По умолчанию 1 (то есть эффективно ВЫКЛЮЧЕНО).
   *
   * 13.07.2026: было 2 (до этого — 8). Пользователь на реальном объекте
   * попросил честный масштаб без исключений: профиль (27/60мм) и подвес
   * должны выглядеть ровно так, как их реальный размер соотносится с
   * масштабом проекта — тонкой чертой издалека, корректно только при
   * реальном приближении камеры (как и должно быть в перспективе, без
   * искусственной помощи). При maxScale=1 функция всегда возвращает 1
   * (см. `Math.min(Math.max(scale, 1), maxScale)` ниже) — раздувания нет
   * вообще, это математически эквивалентно отключению приёма. Сам приём
   * (и параметр maxScale) оставлен в коде намеренно, а не удалён —
   * простой путь вернуть частичное раздувание позже (например, отдельным
   * UI-тумблером \"условные обозначения\" vs \"как в реальности\"), если
   * пользователь передумает — тогда не потребуется писать логику заново,
   * только сменить значение по умолчанию или передать явный maxScale.
   */
  maxScale?: number
}

/**
 * Возвращает коэффициент масштаба (>= 1), на который нужно домножить
 * поперечное сечение тонкого элемента, чтобы его видимый размер на экране
 * не опускался ниже `minPx` пикселей. Если элемент и так достаточно
 * крупный на экране — возвращает 1 (без изменений).
 *
 * Формула: на расстоянии `distanceM` один пиксель вьюпорта соответствует
 * `2 * distanceM * tan(fovYRad / 2) / viewportPxHeight` метров (стандартная
 * геометрия перспективной проекции). Отсюда — какой физический размер
 * нужен, чтобы элемент занимал `minPx` пикселей, и во сколько раз это
 * больше его реального размера.
 */
export function calcMinThicknessScale(params: MinThicknessScaleParams): number {
  const { distanceM, fovYRad, viewportPxHeight, minPx, actualWorldSizeM, maxScale = 1 } = params

  if (actualWorldSizeM <= 0 || distanceM <= 0 || viewportPxHeight <= 0 || fovYRad <= 0 || minPx <= 0) {
    return 1
  }

  const worldPerPixel = (2 * distanceM * Math.tan(fovYRad / 2)) / viewportPxHeight
  const minWorldSize = minPx * worldPerPixel
  const scale = minWorldSize / actualWorldSizeM

  if (!Number.isFinite(scale)) return 1
  return Math.min(Math.max(scale, 1), maxScale)
}
