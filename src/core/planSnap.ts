/**
 * planSnap — привязка курсора к существующим конструкциям при рисовании/
 * редактировании линий плана. Вынесено из FloorPlan.tsx (09.07.2026) для
 * юнит-тестирования и как отдельный модуль слоя `core` (была локальная,
 * непротестированная функция внутри огромного UI-компонента).
 *
 * История бага (см. тест-кейсы ниже): у прямой стены x1/y1/x2/y2 — это ОСЬ
 * (центр по толщине), а не грань. Раньше снап к "концу стены" всегда тянул
 * курсор ровно на ось — то есть на точку посередине толщины стены на её
 * торце. Для стены переменной толщины, примыкающей к более толстой
 * конструкции, это означает, что новую стену невозможно было начертить
 * заподлицо ни с одной из граней: она либо чертилась по оси (и потом
 * "утапливалась" в толщу соседней конструкции с одной стороны), либо
 * приходилось тянуть точку вручную мимо снапа и терять точность.
 *
 * Фикс 1: помимо оси, снап теперь предлагает ещё 4 точки-кандидата на
 * каждом конце линии — угловые точки пересечения торца с ОБЕИМИ гранями
 * (лицевой и тыльной). Пользователь наводит курсор на нужный угол (визуально
 * видно на экране, где физически проходит грань) и получает точную
 * привязку именно к грани, а не к оси — независимо от того, какой толщины
 * сама примыкающая стена. Оба варианта (ось и углы граней) сосуществуют:
 * привязка к оси по-прежнему нужна для продолжения цепочки одинаковых
 * стен встык "ось в ось" (обычный частый случай), а привязка к углам
 * граней — для случая разной толщины (мелкая деталь примыкает к капитальной
 * стене заподлицо по одной из граней).
 *
 * Фикс 2: поправка "флэш" (extraOffsetPx — сдвиг точки ещё на полтолщины
 * НОВОЙ стены, чтобы две параллельные стены не утапливались друг в друга
 * при рисовании бок о бок) раньше срабатывала и при КОЛЛИНЕАРНОМ
 * примыкании — когда новая стена продолжает существующую по той же оси
 * (частый случай: достроить перегородку в разрыве между двумя кусками
 * одной капитальной стены). Направление новой стены в этом случае тоже
 * "почти параллельно" существующей (та же ось), поэтому эвристика
 * срабатывала ошибочно и добавляла лишний отступ — курсор промахивался
 * мимо реального угла грани. Теперь поправка применяется только если
 * проекция курсора на тело линии НЕ зажата по границе (0 < t < 1) —
 * то есть только для настоящего "бок о бок", а не для примыкания к торцу.
 *
 * Фикс 3 (10.07.2026): Фикс 1 выше даёт грани-кандидаты СТАРОЙ стены — то
 * есть возвращает точку, которая физически лежит НА грани старой стены.
 * Но если эту точку напрямую использовать как ОСЬ новой стены (а именно
 * так и хранится PlanLine — x1/y1/x2/y2 это ось, не грань), то для стены
 * ИНОЙ толщины результат неверный: реальная плоскость грани НОВОЙ стены
 * съедет ещё на половину её собственной толщины дальше от старой грани —
 * то есть заподлицо ничего не получится, просто на другую (не туже, что
 * раньше) величину. Живой кейс пользователя: три отрезка одной оси разной
 * толщины (250/150/250мм), нужно, чтобы у всех трёх совпадала ОДНА и та же
 * грань (например, левая) по всей высоте — а при рисовании среднего
 * (150мм) отрезка курсор притягивался либо к оси нижнего (250мм), либо к
 * его угловым точкам грани, но НИ ОДНА из этих привязок не даёт нужного
 * результата для стены другой толщины.
 *
 * Решение: РЯДОМ с "сырыми" угловыми кандидатами (Фикс 1, не трогаем —
 * они отдельно провалидированы тест-кейсом на точное попадание в видимый
 * угол существующей стены даже при разной толщине, см. ниже) добавляется
 * ЕЩЁ один набор из 4 кандидатов — та же идея, но со сдвигом на разницу
 * полутолщин (halfThicknessPx старой стены минус newHalfThicknessPx
 * новой) вместо одной halfThicknessPx старой. Эта точка физически лежит
 * НЕ на видимой грани старой стены, а чуть ВНУТРИ её тела (между осью и
 * ближней гранью) — именно там, докуда нужно довести ОСЬ новой стены,
 * чтобы её СОБСТВЕННАЯ грань (а не центр) совпала с гранью старой.
 * Кандидат осмысленно отличается от "сырого" только когда толщина новой
 * стены уже известна и заметно отличается от старой (иначе просто
 * совпадает с ним — не мешает, не создаёт неоднозначности).
 *
 * Почему добавление, а не замена: заменить формулу целиком было бы
 * проще, но это отняло бы у пользователя возможность прицельно попасть
 * РОВНО в видимый угол старой стены (та самая свобода "мне нужно это
 * делать произвольно", ради которой Фикс 1 и добавлялся) — при замене
 * курсор, наведённый точно на видимый угол, промахивался бы мимо него на
 * разницу полутолщин, то есть переносил бы проблему "мимо, куда целился"
 * из одного сценария в другой, вместо решения. С двумя раздельными
 * кандидатами оба варианта остаются достижимы: чтобы попасть в "сырой"
 * угол — целиться точно в видимую грань; чтобы попасть в "флюш"-точку —
 * целиться чуть дальше, в тело старой стены. Это менее очевидно на глаз,
 * но заметно по превью: толщина новой стены в превью рисования (засечки
 * на конце превью-линии, см. конспект 09.07.2026) сразу показывает,
 * промахнулась ли грань новой стены мимо старой — по этому сигналу
 * пользователь и подводит курсор чуть дальше, до флюш-кандидата.
 */
import type { PlanLine } from '../types'
import { getLineVisual } from '../data/constructionTaxonomy'

export interface SnapResult {
  x: number
  y: number
  snapped: boolean
  d: number
}

/**
 * Привязка точки (x,y) к ближайшему кандидату среди существующих линий:
 * - концы оси (центр по толщине) — для продолжения цепочки "ось в ось";
 * - 4 угловые точки граней на концах линии (для примыкания заподлицо по
 *   грани независимо от толщины новой конструкции, для перпендикулярного
 *   примыкания/поворота);
 * - ещё 4 точки — те же углы, но со сдвигом на разницу полутолщин старой
 *   и новой стены вместо чистой полутолщины старой (для коллинеарного
 *   продолжения оси стеной ДРУГОЙ толщины, когда нужна общая грань, а не
 *   общая ось — см. Фикс 3 выше);
 * - ближайшая грань T-образного примыкания вдоль тела линии.
 */
/**
 * 4 угловые точки грани линии `l`, сдвинутые на `offsetPx` от оси вдоль
 * нормали (nx,ny) — общий помощник для "сырых" угловых кандидатов
 * (offsetPx=halfThicknessPx) и для флюш-кандидатов Фикса 3
 * (offsetPx=halfThicknessPx-newHalfThicknessPx). Вынесено в общую функцию,
 * чтобы не дублировать формулу между snapPoint и getFlushCandidates ниже
 * (последняя нужна для визуальной подсказки — маленького маркера у флюш-
 * точки, см. FloorPlan.tsx, по просьбе пользователя 10.07.2026: невидимая
 * иначе точка "на 50мм внутрь стены" неудобно искать на глаз).
 */
function cornerPoints(l: PlanLine, nx: number, ny: number, offsetPx: number): [number, number][] {
  return [
    [l.x1 + nx * offsetPx, l.y1 + ny * offsetPx],
    [l.x1 - nx * offsetPx, l.y1 - ny * offsetPx],
    [l.x2 + nx * offsetPx, l.y2 + ny * offsetPx],
    [l.x2 - nx * offsetPx, l.y2 - ny * offsetPx],
  ]
}

/**
 * Флюш-кандидаты (Фикс 3, 10.07.2026) для ВСЕХ линий плана сразу — то же,
 * что считает snapPoint внутри себя для ближайшей линии, но здесь отдаётся
 * полный список точек (без привязки к курсору) для визуальной подсказки:
 * маленький маркер у каждой такой точки, пока пользователь рисует стену
 * другой толщины рядом с существующей — иначе точка ничем не обозначена
 * на глаз (лежит внутри тела старой стены, не на её видимой грани).
 */
export function getFlushCandidates(
  lines: PlanLine[], scaleMmPx: number, newHalfThicknessPx: number, excludeId?: string,
): { x: number; y: number }[] {
  if (newHalfThicknessPx <= 0) return []
  const out: { x: number; y: number }[] = []
  for (const l of lines) {
    if (l.id === excludeId) continue
    const dx = l.x2 - l.x1, dy = l.y2 - l.y1
    const len = Math.sqrt(dx * dx + dy * dy)
    const ux = len > 1 ? dx / len : 0, uy = len > 1 ? dy / len : 0
    const nx = -uy, ny = ux
    const vis = getLineVisual(l.type, l.spec?.material, l.spec?.subtype, l.spec?.gapMm)
    const halfThicknessPx = vis.thicknessMm > 0 ? (vis.thicknessMm / 2) / scaleMmPx : 0
    if (halfThicknessPx <= 0 || Math.abs(newHalfThicknessPx - halfThicknessPx) <= 0.01) continue
    const flushOffsetPx = halfThicknessPx - newHalfThicknessPx
    for (const [px, py] of cornerPoints(l, nx, ny, flushOffsetPx)) out.push({ x: px, y: py })
  }
  return out
}

export function snapPoint(
  x: number, y: number, lines: PlanLine[], scaleMmPx: number,
  excludeId?: string, threshPx = 24,
  /** Направление новой стены (если уже известно — второй клик при рисовании
   *  прямой линии, drawing.x1/y1 уже зафиксированы) и её собственная
   *  полутолщина в px — см. комментарий у extraOffset и Фикса 3 выше. */
  refDir?: { dx: number; dy: number }, newHalfThicknessPx = 0,
): SnapResult {
  let best: SnapResult = { x, y, snapped: false, d: Infinity }
  for (const l of lines) {
    if (l.id === excludeId) continue
    const dx = l.x2 - l.x1, dy = l.y2 - l.y1
    const len = Math.sqrt(dx * dx + dy * dy)
    const ux = len > 1 ? dx / len : 0, uy = len > 1 ? dy / len : 0
    const nx = -uy, ny = ux

    const vis = getLineVisual(l.type, l.spec?.material, l.spec?.subtype, l.spec?.gapMm)
    const halfThicknessPx = vis.thicknessMm > 0 ? (vis.thicknessMm / 2) / scaleMmPx : 0

    // Концы оси (центр по толщине) — как и раньше, для стыковки "ось в ось"
    for (const [px, py] of [[l.x1, l.y1], [l.x2, l.y2]] as [number, number][]) {
      const d = dist(x, y, px, py)
      if (d <= threshPx && d < best.d) best = { x: px, y: py, snapped: true, d }
    }

    // Фикс 4 (11.07.2026): угловые точки граней (сырые и флюш) корректны
    // только когда новая стена идёт КОЛЛИНЕАРНО этой линии (продолжение по
    // той же оси, разрыв между кусками одной стены — исходный кейс Фикс 1/3,
    // где митр в принципе не нужен: оси параллельны, wallJoin.applyL для
    // параллельных линий ничего не пересекает). Если же новая стена образует
    // РЕАЛЬНЫЙ угол с этой линией (примыкание под углом, не встык по оси),
    // грань-кандидат — это точка со смещением от ОСИ этой линии на её
    // полтолщины. Если конец новой стены зафиксировать в этой точке, её
    // сохранённая ось разойдётся с осью этой линии ровно на halfThicknessPx —
    // а computeWallJoins() ищет L-стык строго по СОВПАДЕНИЮ ОСЕЙ (допуск
    // JOIN_EPS=3px в wallJoin.ts). Совпадение осей нарушается, и настоящий
    // митровый стык (который для угла КАК РАЗ и нужен, чтобы грани сошлись
    // без нахлёста/зазора) не срабатывает вообще — визуально угол не
    // "стыкуется" (баг с реального объекта: обе стены рисуются каждая со
    // своим независимым торцом вместо общего митра).
    // Решение: при реальном угле (не коллинеарно) грань-кандидаты для ЭТОЙ
    // линии не предлагаются — курсор притягивается к её оси (кандидат уже
    // есть выше, безусловно), и wallJoin сам досчитает правильный митр под
    // фактический угол. Для коллинеарного случая (refDir отсутствует ИЛИ
    // угол между refDir и осью этой линии близок к 0/180°) поведение не
    // меняется — Фикс 1/3 работают как раньше.
    const isAngledCorner = (() => {
      if (!refDir) return false // первый клик — направление ещё не известно
      const rlen = Math.sqrt(refDir.dx * refDir.dx + refDir.dy * refDir.dy)
      if (rlen < 1 || len < 1) return false
      const cosAngle = Math.abs((refDir.dx * ux + refDir.dy * uy) / rlen)
      return cosAngle < Math.cos(20 * Math.PI / 180) // не близко к 0°/180°
    })()

    // Угловые точки граней на обоих концах — для примыкания заподлицо по
    // грани независимо от толщины (см. докстринг файла, Фикс 1). Только для
    // линий с реальной толщиной — у нулевой толщины грань совпадает с осью.
    if (halfThicknessPx > 0 && !isAngledCorner) {
      for (const [px, py] of cornerPoints(l, nx, ny, halfThicknessPx)) {
        const d = dist(x, y, px, py)
        if (d <= threshPx && d < best.d) best = { x: px, y: py, snapped: true, d }
      }

      // Фикс 3 (10.07.2026) — ОТДЕЛЬНЫЙ набор кандидатов, НЕ заменяющий
      // "сырые" углы выше: та же идея, но со сдвигом на разницу полутолщин
      // (halfThicknessPx старой минус newHalfThicknessPx новой) вместо
      // одной halfThicknessPx старой — даёт ось, при которой ГРАНЬ новой
      // стены (не её центр) совпадает с гранью старой. Нужен именно
      // отдельно от "сырых" углов (нельзя просто подменить формулу): те
      // валидированы отдельным тест-кейсом (пользователь должен уметь
      // прицельно попасть РОВНО в видимый угол существующей стены, даже
      // если толщины разные — это самостоятельная, ранее запрошенная
      // возможность) — если добавить только "исправленный" вариант, это
      // отняло бы точное попадание в видимый угол ради кейса, который
      // достижим и так, просто целясь чуть дальше (внутрь толщи старой
      // стены, а не в её грань) — с двумя раздельными кандидатами оба
      // случая остаются доступны, ближайший к курсору просто выигрывает.
      // Кандидат осмысленно отличается от "сырого" только когда толщина
      // новой стены уже известна и заметно отличается от старой — иначе
      // (newHalfThicknessPx=0 или совпадает) он просто совпадёт с "сырым"
      // и не будет лишним/мешающим. Видимая подсказка для этой точки —
      // getFlushCandidates выше, рендерится в FloorPlan.tsx как маркер.
      if (newHalfThicknessPx > 0 && Math.abs(newHalfThicknessPx - halfThicknessPx) > 0.01) {
        const flushOffsetPx = halfThicknessPx - newHalfThicknessPx
        for (const [px, py] of cornerPoints(l, nx, ny, flushOffsetPx)) {
          const d = dist(x, y, px, py)
          if (d <= threshPx && d < best.d) best = { x: px, y: py, snapped: true, d }
        }
      }
    }

    // T-примыкание: снап к БЛИЖНЕМУ РЕБРУ стены (не к оси!) — именно туда
    // физически упирается примыкающая перегородка. Длина при этом сразу
    // получается "в свету" (до грани, не до центра), без отдельной коррекции.
    if (len > 1) {
      const tRaw = ((x - l.x1) * dx + (y - l.y1) * dy) / (len * len)
      const t = Math.max(0, Math.min(1, tRaw))
      const axisX = l.x1 + t * dx, axisY = l.y1 + t * dy

      // Нормаль к оси линии; определяем с какой стороны от оси кликнули,
      // чтобы выбрать БЛИЖНЮЮ грань (ту, с которой приближается курсор)
      const side = (x - axisX) * nx + (y - axisY) * ny
      const faceSign = side >= 0 ? 1 : -1

      // Новая стена идёт ПОЧТИ ПАРАЛЛЕЛЬНО этой линии (±25°) — значит,
      // скорее всего, её ведут ВПЛОТНУЮ рядом (флэш, две стены бок о бок),
      // а не упирают её конец в грань T-образно. Без этой поправки снап
      // тянул точку РОВНО на грань l — а после отрисовки СВОЕЙ толщины
      // новая стена наполовину "утапливалась" в уже существующую (баг:
      // невозможно начертить вплотную две параллельные стены одного
      // материала — притяжение всегда возвращало на грань соседней).
      // Поправка: сдвигаем целевую точку ещё дальше от l, на половину
      // толщины именно НОВОЙ (рисуемой) стены — тогда её грань, а не ось,
      // и коснётся грани l.
      //
      // НО: эта поправка имеет смысл только когда курсор проецируется
      // СТРОГО ВНУТРЬ тела линии l (0 < tRaw < 1, без зажатия) — это и есть
      // "бок о бок" случай. Если tRaw вышел за пределы [0,1] и t зажат по
      // границе (реально примыкаем ровно К ТОРЦУ l), это КОЛЛИНЕАРНОЕ
      // продолжение (стена в стену по одной оси, частый случай — например,
      // достройка перегородки в разрыве между двумя кусками одной стены).
      // Раньше поправка срабатывала и тут (направление новой стены тоже
      // "почти параллельно" l, раз она продолжает её по оси), из-за чего
      // курсор промахивался мимо реального угла грани на лишние пол-толщины
      // новой стены — привязка "гуляла" и казалась случайной/не туда, куда
      // целился пользователь. Проверка tRaw без зажатия отличает "иду
      // рядом" от "продолжаю по оси".
      const clamped = tRaw < 0 || tRaw > 1
      let extraOffsetPx = 0
      if (refDir && !clamped) {
        const rlen = Math.sqrt(refDir.dx * refDir.dx + refDir.dy * refDir.dy)
        if (rlen > 1) {
          const cosAngle = Math.abs((refDir.dx * ux + refDir.dy * uy) / rlen)
          if (cosAngle >= Math.cos(25 * Math.PI / 180)) extraOffsetPx = newHalfThicknessPx
        }
      }

      const faceX = axisX + faceSign * nx * (halfThicknessPx + extraOffsetPx)
      const faceY = axisY + faceSign * ny * (halfThicknessPx + extraOffsetPx)

      const d = dist(x, y, faceX, faceY)
      const bodyThresh = threshPx + halfThicknessPx + extraOffsetPx
      // Кандидат валиден относительно СВОЕГО порога (учитывает толщину ИМЕННО этой стены),
      // а не относительно best.d, который мог уже сжаться из-за другой, не относящейся линии.
      // Фикс 4 (11.07.2026): когда t ЗАЖАТ (clamped) — курсор проецируется
      // ЗА пределы тела линии l, — faceX/faceY вырождаются РОВНО в тот же
      // угол грани на конце l, что и явные corner-кандидаты выше (это один
      // и тот же геометрический случай, только посчитанный другим путём).
      // При реальном угле (isAngledCorner) он подвержен той же проблеме
      // рассинхронизации осей с wallJoin.ts — гасим его точно так же.
      if (d <= bodyThresh && d < best.d && !(clamped && isAngledCorner)) {
        best = { x: faceX, y: faceY, snapped: true, d }
      }
    }
  }
  if (!best.snapped) return { x, y, snapped: false, d: threshPx }
  return best
}

export function snapOrtho(x1: number, y1: number, x: number, y: number): { x: number; y: number } {
  const dx = x - x1, dy = y - y1
  const dist2 = Math.sqrt(dx * dx + dy * dy)
  if (dist2 < 1) return { x, y }
  const angle = Math.atan2(dy, dx) * 180 / Math.PI
  const snapped = Math.round(angle / 45) * 45
  const rad = snapped * Math.PI / 180
  return { x: x1 + Math.cos(rad) * dist2, y: y1 + Math.sin(rad) * dist2 }
}

function dist(x1: number, y1: number, x2: number, y2: number) {
  return Math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2)
}
