// Таблица максимальных высот по Кнауф (таблица 13, металлический каркас)
// Ключ: {wallType}_{profile}_{step}_{thickness}_{group} → макс. высота в мм
//
// group — группа листа обшивки, у Кнауф даёт РАЗНУЮ максимальную высоту
// при одном и том же профиле/шаге/толщине металла:
//   'standard' — КНАУФ-лист (обычный/влагостойкий/огнестойкий/влагоогнестойкий)
//   'premium'  — КНАУФ-Сайлентборд, КНАУФ-лист Сапфир
// Премиум-лист жёстче стандартного ГКЛ, поэтому допустимая высота выше.
//
// Значения проверены построчно по офиц. таблице Кнауф (файлы 013/014/015,
// сессия 04.07.2026) — старые значения (только 'standard') совпали день
// в день, добавлен столбец 'premium', который раньше отсутствовал вовсе
// (т.е. для Сапфира программа раньше занижала допустимую высоту, но
// никогда не завышала — это была безопасная, просто неполная таблица).
//
// С115.1/.2/.3 и С116 (двойной каркас) добавлены той же сессией — сама
// механика расчёта двойного каркаса ЕЩЁ НЕ реализована (это отдельная,
// более крупная задача), но таблица высот уже полная и проверена, чтобы
// не тащить лишний источник ошибок, когда дойдём до самого расчёта.

export type MaxHeightBoardGroup = 'standard' | 'premium'

/**
 * Группа листа для таблицы высот по BoardMaterial.
 * sapphire → premium (подтверждено таблицей Кнауф).
 * gkl (любой subtype) → standard.
 * gvl/aquamarine → standard (КОНСЕРВАТИВНЫЙ дефолт: в таблице Кнауф нет
 * отдельного столбца для ГВЛ/Аквамарин, используем заниженное безопасное
 * значение, а не гадаем).
 * Примечание: КНАУФ-Сайлентборд физически тоже premium, но его пока нет
 * в BoardMaterial (справочник листов) — известный пробел, отдельная задача.
 */
export function resolveMaxHeightGroup(material: string): MaxHeightBoardGroup {
  return material === 'sapphire' ? 'premium' : 'standard'
}

