/**
 * Возвращает максимально допустимую высоту по Кнауф (мм).
 * Если комбинация не найдена — возвращает 0 (проверку не делаем).
 */
export function getMaxHeight(
  wallType: string,
  profile: string,
  step: number,
  thickness: string
): number {
  const key = `${wallType}_${profile}_${step}_${thickness}`
  return MAX_HEIGHT[key] ?? 0
}
