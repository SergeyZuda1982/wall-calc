/**
 * Возвращает максимально допустимую высоту по Кнауф (мм).
 * group по умолчанию 'standard' — безопасный консервативный дефолт для
 * вызовов, которые ещё не передают группу листа явно.
 * Если комбинация не найдена — возвращает 0 (проверку не делаем: это
 * относится к wallType/профилям, для которых таблицы ещё нет, например
 * пока не подключённая механика двойного каркаса 115/116).
 */
export function getMaxHeight(
  wallType: string,
  profile: string,
  step: number,
  thickness: string,
  group: MaxHeightBoardGroup = 'standard',
): number {
  const key = `${wallType}_${profile}_${step}_${thickness}_${group}`
  return MAX_HEIGHT[key] ?? 0
}
