// ─── Официальная таблица КНАУФ "Межосевые расстояния при устройстве каркаса" ─
// Источник для П112 (сессия 11.07.2026, пользователь прислал фото официального
// документа Кнауф "Потолок подвесной на металлическом каркасе", лист
// 1.045.9-2.08.1-2, стр. 30): таблица покрывает ВЕСЬ диапазон c=500..1200 с
// шагом 100, и содержит РОВНО ТРИ колонки нагрузки — ≤0,15 / ≤0,30 / ≤0,50
// кН/м² (колонки 0,40 в реальном документе нет вообще — см. ниже).
//   c (шаг ОСНОВНОГО профиля)  — 500..1200мм, выбирает пользователь
//   b (шаг НЕСУЩЕГО профиля)   — 500мм, единственное значение в этом листе
//                                 ("при поперечном монтаже листов") — у П112
//                                 продольного монтажа в документе нет, это
//                                 не "один из вариантов", а факт данной
//                                 системы (см. КОНСПЕКТ.md, 11.07.2026)
//   a (шаг ПОДВЕСОВ/дюбелей)   — зависит от c И класса нагрузки
//
// ⚠️ ИСТОРИЯ ОШИБКИ (11.07.2026): более ранняя версия этой таблицы (сессия
// 09.07.2026) была неполной по диапазону c (только 800/1000/1200) и включала
// придуманную колонку 0,40 кН/м², а также блок 'lengthwise' (продольный
// монтаж, b=400мм) — по словам пользователя, эти данные, вероятно, результат
// смешения таблиц П112 и П113 на одном из более ранних этапов разработки.
// Официальный документ П112 не содержит ни колонки 0,40, ни продольного
// монтажа — оба варианта здесь убраны для П112. Если П113 (следующая
// сессия, разбор с нуля) действительно предусматривает свою отдельную
// таблицу межосевых расстояний с продольным монтажом — она должна быть
// сверена по СВОЕМУ официальному документу отдельно, а не унаследована
// отсюда.

